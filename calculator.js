// Сложение
function plus() {
    let one = prompt(`Введите первое число`);
    let two = prompt(`Введите второе число`);
    alert(Number(one) + Number(two));
}

// Вычитание
function minus() {
    let one = prompt(`Введите первое число`);
    let two = prompt(`Введите второе число`);
    alert(Number(one) - Number(two));
}

// Умножение
function multiplication() {
    let one = prompt(`Введите первое число`);
    let two = prompt(`Введите второе число`);
    alert(Number(one) * Number(two));
}

// Деление
function division() {
    let one = prompt(`Введите первое число`);
    let two = prompt(`Введите второе число`);
    alert(Number(one) / Number(two));
}